    private void btnAddWordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddWordActionPerformed
        // TODO add your handling code here:
        String textSpelling = txtSpelling.getText();
        String textExplain = txtExplain.getText();
        String textTranscription = txtTranscription.getText();
        String textExample = areaExample.getText();
        String textExplainExample = areaExplainExample.getText();
        boolean flagAdd = true;
        String Message = "";
        if(textSpelling.equals("")){
             Message += "Từ không được rỗng ! \n";
            flagAdd = false;
        }
        if(textExplain.equals("")){
            Message += "Nghĩa không được rỗng! \n";
            flagAdd = false;
        }
        if(textTranscription.equals("")){
            Message += "Phiên âm không được rỗng! \n";
            flagAdd = false;
        }
        if(textExample.equals("")){
            Message += "Câu ví dụ không được rỗng! \n";
            flagAdd = false;
        }
        if(textExplainExample.equals("")){
            Message += "Nghĩa câu ví dụ không được rỗng! \n";
            flagAdd = false;
        }
        if(flagAdd == false){
            JOptionPane.showMessageDialog(null, Message);
        }else{
            boolean checkExistWord = Dictionaty.existWord(textSpelling);
            if(checkExistWord == true){
                JOptionPane.showMessageDialog(null, "Từ này đã tồn tại !");
            }else{
                newWord word = new newWord(textSpelling, textExplain,textTranscription, textExample, textExplainExample);
                Dictionaty.listWord.add(word);
                Dictionaty.setTotalWord(Dictionaty.listWord.size());
                showWordFromFile();
            }
        }
    }//GEN-LAST:event_btnAddWordActionPerformed

