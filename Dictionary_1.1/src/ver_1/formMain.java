    public void loadInfoToTextBox(int indexSelected){
        txtSpelling.setText(Dictionaty.listWord.get(indexSelected).getSpelling());
        txtExplain.setText(Dictionaty.listWord.get(indexSelected).getExplain());
        txtTranscription.setText(Dictionaty.listWord.get(indexSelected).getTranscription());
        areaExample.setText(Dictionaty.listWord.get(indexSelected).getExample());
        areaExplainExample.setText(Dictionaty.listWord.get(indexSelected).getExlainExample());
    }
