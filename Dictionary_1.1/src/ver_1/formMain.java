    /**
    * Hàm hiển thị từ đã load từ file
    */
    public void showWordFromFile(){
        listEnglish.removeAll();
        listNghia.removeAll();
        if (Dictionaty.getTotalWord() == 0) {
            JOptionPane.showMessageDialog(null, "Bạn chưa chọn file load dữ liệu");
        } else {
            for (int i = 0; i < Dictionaty.getTotalWord(); i++) {
                dlmEnglish.addElement(i + ": " + Dictionaty.listWord.get(i).getSpelling());
                dlmNghia.addElement(i + ": " + Dictionaty.listWord.get(i).getExplain());
            }
            listEnglish.setModel(dlmEnglish);
            listNghia.setModel(dlmNghia);
        }
    }
    /**
    * Hàm xoá từ đã được hiển thị
    */
    public void removeAllWord(boolean ShowMessage){
        if(dlmNghia.size() != 0){
            listEnglish.getSelectionModel().clearSelection();
            dlmNghia.removeAllElements();
            dlmEnglish.removeAllElements();
            listEnglish.removeAll();
            listNghia.removeAll();
        }else{
            if(ShowMessage == true){
                JOptionPane.showMessageDialog(null, "Danh sách trống ! Không có phần tử để xoá ");
            }
        }
    }
    
