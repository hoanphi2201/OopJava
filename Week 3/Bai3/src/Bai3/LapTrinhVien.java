package Bai3;

/**
 *
 * @author phixuanhoan
 */
public class LapTrinhVien {
    //Thuộc tính
    private String HoTen;
    private String DiaChi;
    private String KyNang;
    private int Tuoi;
    private int NamTotNghiep;
    
    /**
     * @return the HoTen
     */
    public String getHoTen() {
        return HoTen;
    }

    /**
     * @param HoTen the HoTen to set
     */
    public void setHoTen(String HoTen) {
        this.HoTen = HoTen;
    }

    /**
     * @return the DiaChi
     */
    public String getDiaChi() {
        return DiaChi;
    }

    /**
     * @param DiaChi the DiaChi to set
     */
    public void setDiaChi(String DiaChi) {
        this.DiaChi = DiaChi;
    }

    /**
     * @return the KyNang
     */
    public String getKyNang() {
        return KyNang;
    }

    /**
     * @param KyNang the KyNang to set
     */
    public void setKyNang(String KyNang) {
        this.KyNang = KyNang;
    }

    /**
     * @return the Tuoi
     */
    public int getTuoi() {
        return Tuoi;
    }

    /**
     * @param Tuoi the Tuoi to set
     */
    public void setTuoi(int Tuoi) {
        this.Tuoi = Tuoi;
    }

    /**
     * @return the NamTotNghiep
     */
    public int getNamTotNghiep() {
        return NamTotNghiep;
    }

    /**
     * @param NamTotNghiep the NamTotNghiep to set
     */
    public void setNamTotNghiep(int NamTotNghiep) {
        this.NamTotNghiep = NamTotNghiep;
    }
    
    
}
